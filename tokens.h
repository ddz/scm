/*
 * Scheme Token definitions
 */

#ifndef TOKENS_H
#define TOKENS_H

#define IDENTIFIER 1
#define BOOLEAN    2
#define NUMBER     3
#define CHARACTER  4
#define STRING     5
#define LP         6    /* Left parenthesis '(' */
#define RP         7    /* Right parenthesis ')' */
#define SP         8    /* Sharp parenthesis '#(' */
#define QUOTE      9
#define BACKQUOTE 10
#define COMMA     11
#define COMMAAT   12    /* ",@" */
#define PERIOD    13

#endif
