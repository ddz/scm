/*
 * Scheme Token definitions
 */

#ifndef TOKENS_H
#define TOKENS_H

#define IDENTIFIER 0
#define BOOLEAN    1
#define NUMBER     2
#define CHARACTER  3
#define STRING     4
#define LP         5    /* Left parenthesis '(' */
#define RP         6    /* Right parenthesis ')' */
#define SP         7    /* Sharp parenthesis '#(' */
#define QUOTE      8
#define BACKQUOTE  9
#define COMMA     10
#define COMMAAT   11    /* ",@" */
#define PERIOD    12

#endif
